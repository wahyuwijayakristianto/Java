    private void prosesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prosesActionPerformed
        // TODO add your handling code here:
        int Jumlah = Integer.parseInt(txtjumlah.getText());
        //mengambil teks di kolom jumlah dan di convertkan ke bentuk INT
        try {
            String sql = "select * from buah where kode='" + txtkode.getText() + "'";
            //mengambil semua yang ada di tabel buah (mysql) yang mana kodenya sama dengan teks kode yang kita isi waktu penjualan
            st = conn.createStatement();
            // berfungsi untuk melakukan proses pengiriman statement SQL
            rs = st.executeQuery(sql);
            // melaksanakan perintah st
            rs.next();
            String harga = rs.getString("harga");
            //mengambil nilai harga yang ada di mysql
            String stok = rs.getString("stok");
            //mengambil nilai stok yang ada di mysql
            if (Jumlah <= Integer.parseInt(stok)) {
                //jika jumlah kurang sama dengan stok yang ada 
                txthasil.setText("Nama buah\t: " + rs.getString("nama buah") + "\nHarga\t: " + harga + "\nJumlah beli\t: " + Jumlah);
                //set teks di hasil mengambil nama buah, harga  yang ada di mysql dan jumlah yang kita input di jframenya
                txttotal.setText(Jumlah * Integer.parseInt(harga) + "");
                //set teks jumlah dikali harga 
            } else if (Jumlah > Integer.parseInt(stok)) {
                //selain itu jika jumlah lebih dari stok
                JOptionPane.showMessageDialog(null, "stok tidak tersedia");
            }
            //keluar kotak pesan stok tidak tersedia

        } catch (NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, "data tidak tersedia");
        }


    }//GEN-LAST:event_prosesActionPerformed


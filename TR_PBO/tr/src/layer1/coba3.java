    private void UpdateTabel() {
        try {
            String sql = "SELECT * FROM buah;";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            DefaultTableModel dtm = (DefaultTableModel) tabel2.getModel();
            dtm.setRowCount(0);
            String[] data = new String[4];
            int i = 1;

            while (rs.next()) {
                data[0] = rs.getString("kode");
                data[1] = rs.getString("nama buah");
                data[2] = rs.getString("harga");
                data[3] = rs.getString("stok");
                dtm.addRow(data);
                i++;
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

